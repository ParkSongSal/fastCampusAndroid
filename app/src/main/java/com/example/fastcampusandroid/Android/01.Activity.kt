/*

01. Activity
        - 앱의 한 화면이다.

# Life Cycle (생명주기)
- onCreate
  -> Activity가 만들어질 때 단 한번만 호출된다.
  -> Activity를 만들때 단 한번만 하면 되는 작업들을 여기에서 해준다.

- onStart
- onResume
  -> 다시 앱으로 돌아올떄 무조건 호출 된다.
  -> Activity가 다시 호출될 떄 하면 되는 작업들을 여기에서 해준다.

- onPause
  -> 화면의 일부가 가려졌을때

- onStop
  -> 화면의 전부가 보이지 않을떄

- onDestory

 */
